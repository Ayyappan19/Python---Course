print("Lessthan or Equal <= ", c <= d) 


#03.Greaterthan >

print("Greaterthan >", a > b)  # 20 > 30


# 04.Greaterthan or Equal <=

print("Lessthan or Equal <= ", c >= d) 


# 05.Equal to

print("Equal to ", a == b)


# 06.Not Equal to !=

print("Not Equal",a != b)